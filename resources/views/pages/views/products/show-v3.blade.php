                $(document).on('click', '.quantity .plus, .quantity .minus', function (e) {

                                e.preventDefault();
                                var $purchase = $(this).closest('.quantity').find('.purchase');
                                var $qty = $(this).closest('.quantity').find('.qty'),
                                    currentVal = parseFloat($qty.val()),
                                    max = parseFloat($qty.attr('max')),
                                    min = parseFloat($qty.attr('min')),
                                    step = $qty.attr('step');

                                // Format values
                                if (!currentVal || currentVal === '' || currentVal === 'NaN') currentVal = 0;
                                if (max === '' || max === 'NaN') max = '';
                                if (min === '' || min === 'NaN') min = 0;
                                if (step === 'any' || step === '' || step === undefined || parseFloat(step) === 'NaN') step = 1;

                                // Change the value
                                if ($(this).is('.plus')) {

                                    if (max && ( max == currentVal || currentVal > max )) {
                                        $qty.val(max);
                                    } else {
                                        $qty.val(currentVal + parseFloat(step));
                                    }

                                } else {

                                    if (min && ( min == currentVal || currentVal < min )) {
                                        $qty.val(min); 
                                    } else if (currentVal > 0) {
                                        $qty.val(currentVal - parseFloat(step));
                                    }

                                }

                                var cant = $qty.val();
                                var purchase = $purchase.val();

                                $("#updatePurchases").find("input[name='identifierShopping']").val(purchase);             
                                $("#updatePurchases").find("input[name='identifierQuantity']").val(cant); 
                                $("#detailsShoppings").find("input[name='identifierQuantity']").val(cant);

                                updatePurchases();



                }); 

